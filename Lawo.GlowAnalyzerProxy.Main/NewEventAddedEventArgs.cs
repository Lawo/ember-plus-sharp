namespace Lawo.GlowAnalyzerProxy.Main
{
    using System;

    internal sealed class ScrollEventIntoViewEventArgs : EventArgs
    {
        private readonly Event evt;

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        internal ScrollEventIntoViewEventArgs(Event evt)
        {
            this.evt = evt;
        }

        internal Event NewEvent
        {
            get { return this.evt; }
        }
    }
}
